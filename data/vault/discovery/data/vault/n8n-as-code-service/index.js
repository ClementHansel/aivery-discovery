async function n8nLogin() {
  const n8nHost  = process.env.N8N_HOST          || 'http://localhost:5678';
  const email    = process.env.N8N_ADMIN_EMAIL    || 'admin@aivory.local';
  const password = process.env.N8N_ADMIN_PASSWORD || 'Aivory2026!';
  const res = await fetch(`${n8nHost}/rest/login`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ emailOrLdapLoginId: email, password }),
  });
  if (!res.ok) throw new Error(`Login failed (${res.status}): ${await res.text()}`);
  let rawCookies = [];
  if (typeof res.headers.raw === 'function') {
    rawCookies = res.headers.raw()['set-cookie'] || [];
  } else {
    const sc = res.headers.get('set-cookie');
    if (sc) rawCookies = [sc];
  }
  if (!rawCookies.length) throw new Error('Login OK but no Set-Cookie header returned');
  return rawCookies.map(c => c.split(';')[0].trim()).join('; ');
}


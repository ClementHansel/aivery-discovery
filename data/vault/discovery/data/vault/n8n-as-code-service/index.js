function loadEnvFile(filePath) {
  if (!fs.existsSync(filePath)) return;
  const lines = fs.readFileSync(filePath, 'utf8').split(/\r?\n/);
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const separatorIndex = trimmed.indexOf('=');
    if (separatorIndex === -1) continue;
    const key = trimmed.slice(0, separatorIndex).trim();
    const value = trimmed.slice(separatorIndex + 1).trim().replace(/^["']|["']$/g, '');
    if (key && process.env[key] === undefined) process.env[key] = value;
  }
}

function ensureDraftsDir() {
  fs.mkdirSync(DRAFTS_DIR, { recursive: true });
}

function buildDraftId(value) {
  return value || `draft_${Date.now()}_${crypto.randomBytes(4).toString('hex')}`;
}

function safeFileName(value) {
  return String(value).replace(/[^a-zA-Z0-9_-]/g, '_');
}

function draftPaths(draftId) {
  const safeId = safeFileName(draftId);
  return {
    workflowPath: path.join(DRAFTS_DIR, `${safeId}.workflow.ts`),
    metaPath: path.join(DRAFTS_DIR, `${safeId}.meta.json`),
  };
}

function normalizeSteps(steps) {
  if (!Array.isArray(steps)) return [];
  return steps.map((step, index) => ({
    id: step.id || `step_${index + 1}`,
    type: step.type || (index === 0 ? 'trigger' : 'action'),
    title: step.title || step.action || `Step ${index + 1}`,
    description: step.description || step.output || '',
    nodeType: step.nodeType || null,
    parameters: step.parameters || {},
    typeVersion: step.typeVersion || null,
    config: step.config || {},
    n8nInspection: step.n8nInspection || null,
  }));
}

function buildWorkflowCode({ draftId, intent, steps, config }) {
  const payload = {
    draftId,
    intent,
    generatedAt: new Date().toISOString(),
    source: 'aivory-n8n-as-code-service',
    steps,
    config: config || {},
  };

  return [
    '// Auto-generated by Aivory Workflow Copilot',
    '// This draft is validated in the sandbox before user activation.',
    '',
    `export default ${JSON.stringify(payload, null, 2)};`,
    '',
  ].join('\n');
}


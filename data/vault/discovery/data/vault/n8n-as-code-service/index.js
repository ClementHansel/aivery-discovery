function detectIntent(stepTitle, stepType, stepDescription) {
  const text = `${stepTitle} ${stepType} ${stepDescription || ''}`.toLowerCase();

  const PATTERNS = {
    schedule:  /schedule|cron|daily|hourly|weekly|timer|interval|every\s+\d|jadwal|setiap|berkala|per\s*\d+\s*jam|tiap/,
    email:     /email|mail\b|smtp|inbox|send.*mail/,
    database:  /mysql|postgres|postgresql|sql\b|database|db\b|query|select|insert/,
    http:      /\bhttp\b|\bapi\b|request\b|fetch\b|call.*endpoint|rest\b|post.*url|get.*url/,
    ssh:       /\bssh\b|\bscp\b|\bexec\b|remote.*command|run.*command|shell\b/,
    ftp:       /ftp|sftp|file.*transfer|upload.*file|download.*file/,
    compress:  /compress|zip\b|tar\b|gzip|archive|unzip|extract/,
    cleanup:   /delete\b|remove\b|cleanup|clean.*up|purge\b|clear\b|truncate/,
    filter:    /condition|\bif\b|decision|\bcheck\b|\bfilter\b|branch/,
    transform: /transform|convert|format\b|parse\b|extract\b|set.*value|map.*data/,
    messaging: /slack|discord|telegram|whatsapp|\bsms\b|\bteams\b/,
    respond:   /\brespond\b|\breturn\b|send.*response|reply\b|webhook.*response/,
    webhook:   /webhook|trigger.*http|http.*trigger|on.*request/,
    ai:        /\bai\b|\bllm\b|analyze|generate\b|summarize|classify|\bgpt\b|zeroclaw/,
  };

  // stepType exact match first
  if (stepType && PATTERNS[stepType]) return stepType;

  // Pattern match on full text
  for (const [intent, pattern] of Object.entries(PATTERNS)) {
    if (pattern.test(text)) return intent;
  }

  // Fallback: first step = trigger, rest = transform
  return 'default';
}

function buildRealN8nWorkflow({ draftId, intent, steps }) {
  const nodes      = [];
  const connections = {};
  const GRID_X     = 280;  // horizontal spacing between nodes
  const START_X    = 100;
  const START_Y    = 300;

  // If no steps or empty, fallback to manual trigger only
  if (!steps || steps.length === 0) {
    return {
      name: `Aivory Draft ${draftId}`,
      settings: {},
      nodes: [
        {
          id: 'ManualTrigger_1',
          name: 'Manual Trigger',
          type: 'n8n-nodes-base.manualTrigger',
          typeVersion: 1,
          position: [START_X, START_Y],
          parameters: {},
        },
      ],
      connections: {},
    };
  }

  // Build nodes from steps
  steps.forEach((step, index) => {
    // Use nodeType from step if provided (from ZeroClaw/n8n MCP inspection)
    // Fall back to detectIntent only if nodeType is not available
    let nodeConfig;
    let intent;
    if (step.nodeType) {
      // Direct n8n node type provided — use it directly
      nodeConfig = {
        type: step.nodeType,
        typeVersion: step.typeVersion || 1,
        parameters: step.parameters || {},
      };
      intent = 'direct';
    } else {
      intent = detectIntent(
        step.title || step.action || '',
        step.type  || '',
        step.description || ''
      );
      nodeConfig = NODE_TYPE_MAP[intent] || NODE_TYPE_MAP.default;
    }
    const nodeName   = step.title || `Step ${index + 1}`;
    const nodeId     = `node_${index + 1}_${intent}`;

    nodes.push({
      id:          nodeId,
      name:        nodeName,
      type:        nodeConfig.type,
      typeVersion: nodeConfig.typeVersion,
      position:    [START_X + (index * GRID_X), START_Y],
      parameters:  { ...nodeConfig.parameters },
    });
  });

  // Auto-wire connections: each node output → next node input
  for (let i = 0; i < nodes.length - 1; i++) {
    const fromName = nodes[i].name;
    const toName   = nodes[i + 1].name;

    connections[fromName] = {
      main: [[{ node: toName, type: 'main', index: 0 }]],
    };
  }

  return {
    name:        intent || `Aivory Workflow ${draftId}`,
    settings:    {},
    nodes,
    connections,
  };
}

async function n8nApiRequest(method, apiPath, body) {
  const url = `${N8N_HOST}/api/v1${apiPath}`;
  const headers = { 'Content-Type': 'application/json' };
  if (N8N_API_KEY) headers['X-N8N-API-KEY'] = N8N_API_KEY;

  const response = await fetch(url, {
    method,
    headers,
    body: body ? JSON.stringify(body) : undefined,
  });

  const text = await response.text();
  if (!response.ok) {
    const error = new Error(text.trim() || `${method} ${apiPath} failed`);
    error.status = response.status;
    throw error;
  }

  try {
    return text ? JSON.parse(text) : null;
  } catch {
    return text;
  }
}

function readMeta(draftId) {
  const { metaPath } = draftPaths(draftId);
  if (!fs.existsSync(metaPath)) {
    const error = new Error('draft not found');
    error.status = 404;
    throw error;
  }
  return JSON.parse(fs.readFileSync(metaPath, 'utf8'));
}

function writeMeta(draftId, meta) {
  const { metaPath } = draftPaths(draftId);
  fs.writeFileSync(metaPath, JSON.stringify(meta, null, 2));
}

function inferNodeRequirement(step) {
  const title = step.title || step.action || step.nodeName || step.id || 'Workflow Step';
  const description = step.description || step.output || '';
  const haystack = `${title} ${description}`.toLowerCase();
  const inspectedNode = step.n8nInspection?.selectedNode;

  const definitions = [
    {
      match: /(email|smtp|mail|newsletter)/,
      nodeName: 'Email Delivery',
      nodeType: 'n8n-nodes-base.emailSend',
      credentialType: 'smtpCredentials',
      requiredFields: [
        { key: 'host', label: 'SMTP Host', type: 'text', example: 'smtp.gmail.com' },
        { key: 'port', label: 'SMTP Port', type: 'text', example: '465' },
        { key: 'user', label: 'SMTP Username', type: 'text', example: 'automation@company.com' },
        { key: 'password', label: 'SMTP Password / App Password', type: 'password' },
      ],
    },
    {
      match: /(slack)/,
      nodeName: 'Slack',
      nodeType: 'n8n-nodes-base.slack',
      credentialType: 'slackApi',
      requiredFields: [{ key: 'botToken', label: 'Slack Bot Token', type: 'password' }],
    },
    {
      match: /(notion)/,
      nodeName: 'Notion',
      nodeType: 'n8n-nodes-base.notion',
      credentialType: 'notionApi',
      requiredFields: [{ key: 'apiKey', label: 'Notion Internal Integration Token', type: 'password' }],
    },
    {
      match: /(crm|hubspot|salesforce|pipedrive)/,
      nodeName: 'CRM',
      nodeType: 'n8n-nodes-base.httpRequest',
      credentialType: 'httpHeaderAuth',
      requiredFields: [
        { key: 'baseUrl', label: 'CRM Base URL', type: 'text', example: 'https://api.hubapi.com' },
        { key: 'apiKey', label: 'CRM API Key / Token', type: 'password' },
      ],
    },
    {
      match: /(sheet|spreadsheet|google sheet|sheets)/,
      nodeName: 'Google Sheets',
      nodeType: 'n8n-nodes-base.googleSheets',
      credentialType: 'googleSheetsOAuth2Api',
      requiredFields: [{ key: 'oauth', label: 'Google Sheets OAuth', type: 'oauth' }],
    },
    {
      match: /(http|api|webhook|rest)/,
      nodeName: 'HTTP Request',
      nodeType: 'n8n-nodes-base.httpRequest',
      credentialType: 'httpHeaderAuth',
      requiredFields: [
        { key: 'baseUrl', label: 'API Base URL', type: 'text', example: 'https://api.example.com' },
        { key: 'apiKey', label: 'API Key / Bearer Token', type: 'password' },
      ],
    },
  ];

  const matched = definitions.find((definition) => definition.match.test(haystack));
  if (!matched && inspectedNode && step.n8nInspection?.schema?.credentials) {
    return {
      nodeId: step.id || title.toLowerCase().replace(/[^a-z0-9]+/g, '_'),
      nodeName: inspectedNode.displayName || title,
      nodeType: inspectedNode.workflowNodeType || inspectedNode.nodeType || 'unknown',
      credentialType: step.n8nInspection?.schema?.credentials ? 'credential_required' : undefined,
      requiredFields: [],
    };
  }
  if (!matched) return null;

  return {
    nodeId: step.id || title.toLowerCase().replace(/[^a-z0-9]+/g, '_'),
    nodeName: matched.nodeName,
    nodeType: matched.nodeType,
    credentialType: matched.credentialType,
    requiredFields: matched.requiredFields,
  };
}

function buildSetupReport(meta) {
  const nodeRequirements = meta.steps.map(inferNodeRequirement).filter(Boolean);
  return {
    workflowId: meta.draftId,
    workflowName: meta.intent || 'Aivory Workflow Draft',
    readyToDeploy: nodeRequirements.length === 0,
    nodeRequirements,
    summary: nodeRequirements.length === 0
      ? 'No external credentials were inferred from the current draft.'
      : `This draft needs ${nodeRequirements.length} credential or configuration setup item(s) before activation.`,
  };
}


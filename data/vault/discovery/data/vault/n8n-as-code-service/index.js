async function n8nRunWorkflow(workflowId, workflowData, cookie) {
  const n8nHost = process.env.N8N_HOST || 'http://localhost:5678';
  const nodes   = workflowData.nodes || [];
  
  // Safer trigger detection with priority order
  const TRIGGER_PRIORITY = [
    'n8n-nodes-base.manualTrigger',
    'n8n-nodes-base.scheduleTrigger',
    'n8n-nodes-base.webhook',
    'n8n-nodes-base.webhookTrigger',
  ];
  
  const trigger = TRIGGER_PRIORITY.reduce((found, type) =>
    found ?? nodes.find(n => n.type === type), null
  ) ?? nodes.find(n =>
    n.type?.toLowerCase().includes('trigger') &&
    !n.type?.toLowerCase().includes('error')
  ) ?? nodes[0];
  const res = await fetch(`${n8nHost}/rest/workflows/${workflowId}/run`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json', 'Cookie': cookie },
    body: JSON.stringify({
      startNodes:      [trigger?.name || 'Manual Trigger'],
      destinationNode: '',
      runData:         null,
      pinData:         {},
      workflowData:    workflowData,
    }),
  });
  if (!res.ok) throw new Error(`Run failed (${res.status}): ${await res.text()}`);
  const data = await res.json();
  const executionId = data?.data?.executionId ?? data?.executionId;
  if (!executionId) throw new Error(`No executionId in response: ${JSON.stringify(data)}`);
  return String(executionId);
}

async function n8nPollExecution(executionId, cookie) {
  const n8nHost  = process.env.N8N_HOST || 'http://localhost:5678';
  const deadline = Date.now() + 30000;
  while (Date.now() < deadline) {
    const res  = await fetch(`${n8nHost}/rest/executions/${executionId}`, {
      headers: { 'Cookie': cookie },
    });
    if (!res.ok) throw new Error(`Poll failed (${res.status})`);
    const exec = (await res.json())?.data;
    
    // Handle non-recoverable states
    if (exec?.status === 'waiting') {
      // Workflow paused at a Wait node — treat as partial success
      exec.finished = true;
    }
    if (exec?.status === 'crashed' || exec?.status === 'canceled') {
      // Non-recoverable — exit immediately with error
      throw new Error(`Execution ${exec.status}: workflow did not complete`);
    }
    
    if (exec?.finished === true) return exec;
    await new Promise(r => setTimeout(r, 1000));
  }
  throw new Error('Timeout: execution did not finish in 30 seconds');
}

function deserializeN8nRunData(dataStr) {
  if (!dataStr || typeof dataStr !== 'string') return null;
  try {
    const arr     = JSON.parse(dataStr);
    if (!Array.isArray(arr)) return null;
    const resolve = v => (typeof v === 'string' && /^\d+$/.test(v) && +v < arr.length)
      ? arr[+v] : v;
    const runDataMap = resolve(resolve(resolve(arr[0])?.resultData)?.runData);
    if (!runDataMap || typeof runDataMap !== 'object') return null;
    const out = {};
    for (const [name, ref] of Object.entries(runDataMap)) {
      const run    = resolve(ref);
      if (!run) continue;
      const status = resolve(run.executionStatus) || 'unknown';
      const errRaw = run.error ? resolve(run.error) : null;
      out[name] = {
        status,
        executionTime: run.executionTime ?? null,
        ...(errRaw && { error: typeof errRaw === 'string' ? errRaw : errRaw?.message ?? JSON.stringify(errRaw) }),
      };
    }
    return Object.keys(out).length ? out : null;
  } catch { return null; }
}

function stripCredentialsForSandbox(workflowDef) {
  // Deep clone
  const wf = JSON.parse(JSON.stringify(workflowDef));

  // Nodes that need credential stripping — replace with Set node
  // so n8n can execute without credential errors
  const CREDENTIAL_NODE_TYPES = [
    'n8n-nodes-base.mySql',
    'n8n-nodes-base.postgres',
    'n8n-nodes-base.emailSend',
    'n8n-nodes-base.ssh',
    'n8n-nodes-base.ftp',
    'n8n-nodes-base.slack',
    'n8n-nodes-base.notion',
    'n8n-nodes-base.googleSheets',
  ];

  wf.nodes = wf.nodes.map(node => {
    // Remove any credential fields
    const stripped = { ...node };
    delete stripped.credentials;

    // If node requires credentials, swap to a harmless Set node
    // that proves the logic flow works without credential errors
    if (CREDENTIAL_NODE_TYPES.includes(node.type)) {
      return {
        id:          stripped.id,
        name:        stripped.name,
        type:        'n8n-nodes-base.set',
        typeVersion: 3.4,
        position:    stripped.position,
        parameters: {
          mode: 'manual',
          assignments: {
            assignments: [{
              id:    '1',
              name:  'sandbox_note',
              value: `[SANDBOX] ${node.name} — requires credential in production`,
              type:  'string',
            }]
          },
        },
      };
    }

    return stripped;
  });

  return wf;
}

async function runSandboxTest(workflowDefinition) {
  const n8nHost = process.env.N8N_HOST    || 'http://localhost:5678';
  const apiKey  = process.env.N8N_API_KEY || '';
  const logs    = [];
  let   wfId    = null;
  const log     = m => { logs.push(m); console.log(`[sandbox] ${m}`); };

  try {
    log('Creating sandbox workflow...');
    // Strip credentials so n8n can execute logic without credential errors
    // Credential validation is done separately via /drafts/bind-credentials
    const sandboxWorkflow = stripCredentialsForSandbox(workflowDefinition);
    log(`Stripped credentials from ${sandboxWorkflow.nodes.length} nodes for sandbox`);
    const cr = await fetch(`${n8nHost}/api/v1/workflows`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json', 'X-N8N-API-KEY': apiKey },
      body: JSON.stringify({
        ...sandboxWorkflow,
        settings: { ...(sandboxWorkflow.settings ?? {}), executionOrder: 'v1' },
      }),
    });
    if (!cr.ok) throw new Error(`Create failed (${cr.status}): ${await cr.text()}`);
    wfId = String((await cr.json())?.id ?? '');
    if (!wfId) throw new Error('No workflow ID returned from create');
    log(`Created: ${wfId}`);

    log('Logging in to n8n...');
    const cookie = await n8nLogin();
    log('Login OK');

    log('Fetching full workflow data (required for /run)...');
    const wfData = await n8nFetchWorkflow(wfId);
    log(`Fetched: ${wfData.nodes?.length} nodes`);

    log('Triggering execution...');
    const execId = await n8nRunWorkflow(wfId, wfData, cookie);
    log(`Execution started: ${execId}`);

    log('Waiting for result (max 30s)...');
    const exec = await n8nPollExecution(execId, cookie);
    log(`Finished: status=${exec.status}`);

    const runMap      = deserializeN8nRunData(exec?.data);
    const nodeResults = runMap
      ? Object.entries(runMap).map(([name, info]) => {
          // If node-level status is unknown, trust overall exec status
          const nodeStatus = (info.status && info.status !== 'unknown')
            ? (info.status === 'success' ? 'success' : 'error')
            : (exec.status === 'success' ? 'success' : 'error');
          return {
            nodeId: name, nodeName: name,
            status: nodeStatus,
            executionTime: info.executionTime ?? null,
            ...(info.error && { error: info.error }),
          };
        })
      : (exec?.workflowData?.nodes || []).map(n => ({
          nodeId: n.id, nodeName: n.name,
          status: exec.status === 'success' ? 'success' : 'error',
        }));

    nodeResults.forEach(n =>
      log(`  ${n.status === 'success' ? 'OK' : 'FAIL'} "${n.nodeName}"${n.error ? ': ' + n.error : ''}`)
    );

    // Annotate nodes that were credential-stripped in sandbox
    const credentialNodeTypes = [
      'n8n-nodes-base.mySql',
      'n8n-nodes-base.postgres',
      'n8n-nodes-base.emailSend',
      'n8n-nodes-base.ssh',
      'n8n-nodes-base.ftp',
      'n8n-nodes-base.slack',
      'n8n-nodes-base.notion',
      'n8n-nodes-base.googleSheets',
    ];
    const strippedNodeNames = (workflowDefinition.nodes || [])
      .filter(n => credentialNodeTypes.includes(n.type))
      .map(n => n.name);
    const annotatedResults = nodeResults.map(n => ({
      ...n,
      ...(strippedNodeNames.includes(n.nodeName) && {
        sandboxNote: 'credential-stripped: logic tested, credential required in production'
      })
    }));

    // DELETE must happen AFTER polling
    log(`Deleting sandbox workflow ${wfId}...`);
    await fetch(`${n8nHost}/api/v1/workflows/${wfId}`, {
      method: 'DELETE', headers: { 'X-N8N-API-KEY': apiKey },
    });
    wfId = null;
    log('Deleted');

    const passed = exec.status === 'success';
    return {
      status:         passed ? 'passed' : 'failed',
      validationMode: 'real_execution',
      executionId:    execId,
      nodeResults:    annotatedResults,
      errors: annotatedResults
        .filter(n => n.status === 'error')
        .map(n => `"${n.nodeName}": ${n.error ?? 'execution failed'}`),
      logs,
    };

  } catch (err) {
    log(`ERROR: ${err.message}`);
    if (wfId) {
      try {
        await fetch(`${n8nHost}/api/v1/workflows/${wfId}`, {
          method: 'DELETE', headers: { 'X-N8N-API-KEY': apiKey },
        });
        log('Emergency cleanup done');
      } catch (e) { log(`Emergency cleanup failed: ${e.message}`); }
    }
    return {
      status: 'error', validationMode: 'real_execution',
      nodeResults: [], errors: [err.message], logs,
    };
  }
}

